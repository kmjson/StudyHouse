    <h4>4. Navigate to Google Chrome extensions</h4>
    <img src="/about/asset_2.png" class="small-image">
    <h4>5. Click Unload 'Load unpacked'</h4>
    <img src="/about/asset_3.png" class="big-image">
    <h4>6. Select the folder 'StudyHouse-Extension-main'</h4>
    <img src="/about/asset_4.png" class="big-image">
    <h4>7. Enable the extension</h4>
    <img src="/about/asset_5.png" class="small-image">
